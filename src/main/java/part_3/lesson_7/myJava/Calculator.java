package part_3.lesson_7.myJava;

public class Calculator {

    public int sum(int argOne, int argTwo){
        return argOne+argTwo;
    }

    public int multiply(int argOne, int argTwo){
        return argOne*argTwo;
    }

    public double divide(int argOne, int argTwo){
        return (double)argOne/(double)argTwo;
    }
}
